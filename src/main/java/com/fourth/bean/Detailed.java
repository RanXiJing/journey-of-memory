package com.fourth.bean;

public class Detailed {
   
    private Integer detailedId;

   
    private String detailedDay;

   
    private String detailedPrice;

    
    private String detailedTravelway;

    
    public Integer getDetailedId() {
        return detailedId;
    }

   
    public void setDetailedId(Integer detailedId) {
        this.detailedId = detailedId;
    }

  
    public String getDetailedDay() {
        return detailedDay;
    }

   
    public void setDetailedDay(String detailedDay) {
        this.detailedDay = detailedDay;
    }

   
    public String getDetailedPrice() {
        return detailedPrice;
    }

    
    public void setDetailedPrice(String detailedPrice) {
        this.detailedPrice = detailedPrice;
    }

   
    public String getDetailedTravelway() {
        return detailedTravelway;
    }

    
    public void setDetailedTravelway(String detailedTravelway) {
        this.detailedTravelway = detailedTravelway;
    }
}